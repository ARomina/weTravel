package ar.edu.unlam.tallerweb1.modelo;

public class Items {
	
	private Long id;
	private String detalles;
	
	//Constructor
	public Items(){
	}

	//Getters and Setters
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getDetalles() {
		return detalles;
	}

	public void setDetalles(String detalles) {
		this.detalles = detalles;
	}

}
